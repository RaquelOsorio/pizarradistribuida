    public void CargarDatos(VOCriterio c) {
        this.nombre.setText(c.getNombre());
        this.descripcion.setText(c.getDescripcion());
        this.c1.setText(c.getConcepto()[0]);
        this.c2.setText(c.getConcepto()[1]);
        this.c3.setText(c.getConcepto()[2]);
        this.c4.setText(c.getConcepto()[3]);
        this.c5.setText(c.getConcepto()[4]);
        this.c6.setText(c.getConcepto()[5]);
        this.c7.setText(c.getConcepto()[6]);
    }

    public JLabel getEstado() {
        return estado;
    }

    @Action
    public void ModificarPresionado() throws RemoteException {
        String conc[] = {this.c1.getText(), this.c2.getText(), this.c3.getText(), this.c4.getText(), this.c5.getText(), this.c6.getText(), this.c7.getText()};
        VOCriterio x = new VOCriterio();
        x.setNombre(this.nombre.getText());
        x.setDescripcion(this.descripcion.getText());
        x.setConcepto(conc);
        control.ModificarCriterio(x);
    }

    @Action
    public void cerrar() throws RemoteException {
        control.MataVista();
    }

    public void VentanaFeedback(String m, String t) throws RemoteException {
        JOptionPane.showMessageDialog(this, m, t, JOptionPane.INFORMATION_MESSAGE);
    }
